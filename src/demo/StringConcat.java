package demo;
import java.util.*;

public class StringConcat {//把字符串连接起来
	   public static void main(String args[])
	   {
	      String s1 = " The Truth";
	      String s2 = " The Way";
	      String s3 = " and The Life";
	      String s4 = s1 + s2 + s3;
	      System.out.println(s4);
	      System.out.println("1" + s1);      
	   }

}
